    private void scheduleClassSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitActionPerformed

        String selectedClass = (String) selectClassComboBox.getSelectedItem();
        String studentID = ((StudentEntry) selectStudentComboBox.getSelectedItem()).getStudentID();

        int reservedSeats = ScheduleQueries.getScheduleByStudentCount(currentSemester, selectedClass);
        int classSeats = ClassQueries.getClassSeats(currentSemester, selectedClass);
        // declare a timestamp field.
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        String status = "Scheduled";

        if (reservedSeats >= classSeats) {

            System.out.println("Scheduling successful");
            status = "Waitlisted";
        }
        ScheduleEntry entry = new ScheduleEntry(currentSemester, selectedClass, studentID, status, currentTimestamp);
        ScheduleQueries.addSchedule(entry);
        ScheduleClassLabel.setText("Class has been" + " " + status);
    }//GEN-LAST:event_scheduleClassSubmitActionPerformed

