    private void submitDropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitDropStudentButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        StudentEntry student = (StudentEntry) dropStudentAdminComboBox.getSelectedItem();
        String studentID = student.getStudentID();
        StudentQueries.dropStudent(studentID);
        for (String semester : semesters) {
            dropStudentTextArea.append("For semester" + semester + "\n");
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            for (ScheduleEntry entry : schedule) {
                String status = entry.getStatus();
                String courseCode = entry.getCourseCode();
                ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByClass(semester, courseCode);
                if ("Scheduled".equals(status)) {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, entry.getStudentID(), entry.getCourseCode());
                    dropStudentTextArea.append("Student " + student + " has been dropped from " +  entry.getCourseCode() + "\n");
//add to the text what / who is being dropped
                    if (!waitlisted.isEmpty()) {
                        ScheduleQueries.updateScheduleEntry(waitlisted.get(0));
                        StudentEntry waitlist = StudentQueries.getStudent(waitlisted.get(0).getStudentID());
                        //add to the text box who was being scheduled from the waitlist 
                        dropStudentTextArea.append("Student " + waitlist + " has been scheduled from the waitlist for " + entry.getCourseCode() + "\n");

                    }

                } else {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, entry.getStudentID(), entry.getCourseCode());
                    //write the text of whos dropped 
                    dropStudentTextArea.append("Student " + student + " has been dropped from waitlist\n");
                }

            }

            
            //look at each entry and find out wether they were waitlisted 
            //if they were waitlisted, put they were dropped form waitlist
            //if they were scheduled, if the waitlkist is not empty, schedule the first person in that list to take them off 
            //after you get out of the loop, call two queires: one for schedule entries one for drop class, one for drop class in class queries 
        }


    }//GEN-LAST:event_submitDropStudentButtonActionPerformed

